#!/bin/bash
# BASH SCRIPT TO SHOW SYSTEM INFORMATION
nouser=`who | wc -l`;
echo "";
echo "------------------------------------------------------------------------------------";
echo "		********** SYSTEM INFORMATION ***********";
echo "------------------------------------------------------------------------------------";
echo "USERNAME : $USER";
echo "LOGUSER  : $LOGNAME";
echo "HOSTNAME : $HOSTNAME";
echo "PRESENT WORKING DIRECTORY: $PWD";
echo "HOME DIRECTORY: $HOME"
echo "CURRENT SHELL: $SHELL";
echo "OSTYPE: $OSTYPE";
echo "OS VERSION: `cat /proc/version`"
echo "NO. OF USERS: $nouser";
echo "------------------------------------------------------------------------------------";
if [ -f /etc/shells ]
then
	echo "------------------------------------------------------------------------------------";
	echo "			*******AVAILABLE SHEELS******";
	echo "------------------------------------------------------------------------------------";
	echo "`cat /etc/shells`"
	echo "------------------------------------------------------------------------------------";
fi;
if [ -f /proc/cpuinfo ]
then
	echo "------------------------------------------------------------------------------------";
	echo "			********CPU INFORMAION******";
	echo "------------------------------------------------------------------------------------";
	echo "`cat /proc/cpuinfo`";
	echo "------------------------------------------------------------------------------------";
fi;
if [ -f /proc/meminfo ]
then
	echo "------------------------------------------------------------------------------------";
	echo "			********MEMORY INFORMAION******";
	echo "------------------------------------------------------------------------------------";
	echo "`cat /proc/meminfo`";
	echo "------------------------------------------------------------------------------------";
fi;
if [ -f /proc/filesystems ]
then
	echo "------------------------------------------------------------------------------------";
	echo "			********FILE SYSTEMS ******";
	echo "------------------------------------------------------------------------------------";
	echo "`cat /proc/filesystems`";
	echo "------------------------------------------------------------------------------------";
fi;
if [ -f /proc/mounts ]
then
	echo "------------------------------------------------------------------------------------";
	echo "			********MOUNTS INFORMATION ******";
	echo "------------------------------------------------------------------------------------";
	echo "`cat /proc/mounts`";
	echo "------------------------------------------------------------------------------------";
fi;

